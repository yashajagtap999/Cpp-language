//Switch :-- It switches from one condition to other .
#include<iostream>
using namespace std;
int main()
{
	int sample;
	cout<<"enter the value";
	cin>>sample;
	switch(sample)
	{
		case 1:
			cout<<"enter 1";
			break;
		case 2:
			cout<<"enter 2";
			break;
		case 3:
			cout<<"enter 3";
			break;
		default:
			cout<<"out of loop";
	}
	
}


